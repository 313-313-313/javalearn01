package j12_Loops.L01_ForLoop.Tasks;

public class Task06_Nested {
    public static void main(String[] args) {

        // 1x1=1
        // 1x2=2
        // 1x3=3
        // 1x4=4
        // 1x5=5
        // 1x6=6
        // 1x7=7
        // 1x8=8
        // 1x9=9
        // 1x10=10
        //yukarıdaki çarpım tablosunu 10 a kadar print eden code create ediniz.

        for(int i=1;i<=10;i++){
            for(int j=1;j<=10;j++){


                System.out.println(i+" x "+j+"="+ (i*j));
            }
            System.out.println();
        }








    }
}
